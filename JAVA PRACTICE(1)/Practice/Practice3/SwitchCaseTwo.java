public class SwitchCaseTwo {

    public static void main(String[] args) {
        String dog = "pomeranian";

        switch (dog) {
            case "pomeranian":
                System.out.println("pomeranian");
                break;
            case "great dane":
                System.out.println("great dane");
                break;
            default:
                System.out.println("put a different type");
        }
    }
    
}
