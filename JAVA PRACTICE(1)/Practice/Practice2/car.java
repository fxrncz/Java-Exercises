class Car {
    private String color;
    private String type;
    private String size;

    public Car() {
        this.color = "Black";
        this.type = "Truck";
        this.size = "Big";
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }
}
