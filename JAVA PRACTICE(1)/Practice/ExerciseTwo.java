public class ExerciseTwo {
    
    public static void main(String[] args) {
        int firstNumber, secondNumber;

        firstNumber = 3;
        secondNumber = 2;

        System.out.println("FirstNumber: " + firstNumber);
        System.out.println("SecondNumber: " + secondNumber);

        int sum = 3 + 2;
        int diff = 3 - 2;
        int pro = 3 * 2;
        int quo = 3 / 2;

        System.out.println("The sum of 3 and 2 is: " + sum);
        System.out.println("The difference of 3 and 2 is: " + diff);
        System.out.println("The product of 3 and 2 is: " + pro);
        System.out.println("The quotient of 3 and 2 is: " + quo);
    }
}
